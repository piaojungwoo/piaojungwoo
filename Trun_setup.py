# 화면 구성 요소의 속성

import turtle as t

t.setup(500,500)
t.bgcolor('orange')

ts=t.Turtle()
ts.shape('circle')
ts.color('green')
ts.up()
ts.goto(0,-200)

te=t.Turtle()
te.shape('turtle')
te.color('red')
te.up()
te.goto(0,200)

t.shape('turtle')
t.color('white')



